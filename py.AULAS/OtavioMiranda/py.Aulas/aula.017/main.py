"""
Operadores De Comparação (Relacionais)

OPERADORES      SIGNIFICADO        EXEMPLO
>               MAIOR QUE          10 > 5
>=              MAIOR OU IGUAL A   10 >= 10
<               MENOR QUE          10 < 5
<=              MENOR OU IGUAL A   10 <= 10
==              IGUAL A            10 == 10
!=              DIFERENTE DE       10 != 5

"""

print("===" * 10)
maior = 10 > 5
print(maior)

maior_ou_igual = 2 >= 2
print(maior_ou_igual)

menor = 10 < 5
print(menor)

menor_ou_igual = 10 <= 10
print(menor_ou_igual)

igual = 10 == 10
print(igual)

diferente = 10 != 5
print(diferente)
print("===" * 10)
